export type IconName = keyof typeof ICON_PATHS;

/** Bir ikonun iç SVG markup'ını döner (bilinmeyen ad → info). */
export function iconInner(name: string): string {
  return ICON_PATHS[name] ?? ICON_PATHS.info;
}

/** İmperatif DOM (el.innerHTML) için tam <svg> string'i üretir. stroke=currentColor, fill=none. */
export function iconSvgMarkup(
  name: string,
  opts?: { size?: number; strokeWidth?: number },
): string {
  const size = opts?.size ?? 24;
  const sw = opts?.strokeWidth ?? 2;
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="${sw}" stroke-linecap="round" stroke-linejoin="round">${iconInner(name)}</svg>`;
}

/** React ikon bileşeni. `currentColor` kullanır — rengi metin/`text-*` sınıfından alır. */
export function Icon({
  name,
  size = 20,
  strokeWidth = 2,
  className,
}: {
  name: string;
  size?: number;
  strokeWidth?: number;
  className?: string;
}) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      aria-hidden="true"
      dangerouslySetInnerHTML={{ __html: iconInner(name) }}
    />
  );
}

